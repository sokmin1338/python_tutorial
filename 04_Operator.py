print(1+1) #2
print(10-4) #6
print(21*10) #210
print(21/3) # 7
print(10**2) # 100 #제곱
print(10%3) # 1 #나머지 구하기
print(10//3) #3 몫 구하기
print(5>3) #True
print(5<3) #False
print(5 >= 4) #True
print(5 <= 4) #False
print(1021 == 1021) #True
print(1021 == 1022) #False
print(1021+1022 == 2043 ) #True
print(1 != 2) #True # 같지 않다.
print(not (1 != 3)) #False
print((3<0) and (3<5)) #False
print((3<0) & (3<5)) #False #둘 중에 하나라도 틀리면 False
print((3<0) or (3<5)) #True #둘 중에 하나라도 맞으면 True
print((3<0) | (4>3)) #True 


